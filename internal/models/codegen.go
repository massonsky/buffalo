// toSnakeCase converts CamelCase to snake_case.
// Handles consecutive uppercase (acronyms) correctly:
//
//	"GPSData"  → "gps_data"
//	"HTTPSHandler" → "https_handler"
//	"UserID" → "user_id"
//	"BoatTelemetryMessage" → "boat_telemetry_message"
func toSnakeCase(s string) string {
	runes := []rune(s)
	var b strings.Builder
	for i, r := range runes {
		if r >= 'A' && r <= 'Z' {
			if i > 0 {
				prev := runes[i-1]
				prevIsUpper := prev >= 'A' && prev <= 'Z'
				nextIsLower := i+1 < len(runes) && runes[i+1] >= 'a' && runes[i+1] <= 'z'
				if !prevIsUpper || nextIsLower {
					b.WriteByte('_')
				}
			}
			b.WriteRune(r + 32)
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}

// toPascalCase converts snake_case to PascalCase.
func toPascalCase(s string) string {
	parts := strings.Split(s, "_")
	for i := range parts {
		if len(parts[i]) > 0 {
			parts[i] = strings.ToUpper(parts[i][:1]) + parts[i][1:]
		}
	}
	return strings.Join(parts, "")
}

// toCamelCase converts snake_case to camelCase.
func toCamelCase(s string) string {
	p := toPascalCase(s)
	if len(p) == 0 {
		return p
	}
	return strings.ToLower(p[:1]) + p[1:]
}

// goPackageName extracts the last component of a dotted package name.
func goPackageName(pkg string) string {
	parts := strings.Split(pkg, ".")
	return parts[len(parts)-1]
}

// header returns the standard "DO NOT EDIT" header.
func header(generator string) string {
	return fmt.Sprintf("// Code generated by %s. DO NOT EDIT.\n", generator)
}

// pythonHeader returns the standard Python "DO NOT EDIT" header.
func pythonHeader(generator string) string {
	return fmt.Sprintf("# Code generated by %s. DO NOT EDIT.\n", generator)
}

// protoTypeToGo maps proto types to Go types.
func protoTypeToGo(protoType string, nullable bool) string {
	// Well-known types
	if wkt, ok := wellKnownTypeGo(protoType); ok {
		if nullable {
			return "*" + wkt
		}
		return wkt
	}
	base := ""
	switch protoType {
	case "string":
		base = "string"
	case "int32", "sint32", "sfixed32":
		base = "int32"
	case "int64", "sint64", "sfixed64":
		base = "int64"
	case "uint32", "fixed32":
		base = "uint32"
	case "uint64", "fixed64":
		base = "uint64"
	case "float":
		base = "float32"
	case "double":
		base = "float64"
	case "bool":
		base = "bool"
	case "bytes":
		base = "[]byte"
	default:
		base = stripPackagePrefix(protoType) // custom message type
	}
	if nullable && base != "[]byte" {
		return "*" + base
	}
	return base
}

