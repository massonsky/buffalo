// NewModelCodeGenerator creates a code generator for the given language + ORM.
func NewModelCodeGenerator(language string, orm ORMPlugin) (ModelCodeGenerator, error) {
	switch language {
	case "python":
		return newPythonGenerator(orm)
	case "go":
		return newGoGenerator(orm)
	case "rust":
		return newRustGenerator(orm)
	case "cpp":
		return newCppGenerator(orm)
	default:
		return nil, fmt.Errorf("unsupported model codegen language: %s", language)
	}
}

