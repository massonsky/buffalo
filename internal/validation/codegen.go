// GoCodeGenerator generates Go Validate() methods.
type GoCodeGenerator struct{}

func (g *GoCodeGenerator) Language() string { return "go" }

func (g *GoCodeGenerator) Generate(messages []MessageRules) ([]GeneratedFile, error) {
	var files []GeneratedFile
	for _, msg := range messages {
		if msg.Disabled {
			continue
		}
		code, err := g.generateForMessage(msg)
		if err != nil {
			return nil, fmt.Errorf("go codegen for %s: %w", msg.MessageName, err)
		}
		files = append(files, GeneratedFile{
			Path:    toSnakeCase(msg.MessageName) + ".validate.go",
			Content: code,
		})
	}
	return files, nil
}

func (g *GoCodeGenerator) generateForMessage(msg MessageRules) (string, error) {
	var b strings.Builder

	b.WriteString("// Code generated by buffalo-validate. DO NOT EDIT.\n")
	b.WriteString(fmt.Sprintf("package %s\n\n", goPackageName(msg.Package)))

	// Collect required imports
	imports := g.resolveImports(msg)
	if len(imports) > 0 {
		b.WriteString("import (\n")
		for _, imp := range imports {
			b.WriteString(fmt.Sprintf("\t%q\n", imp))
		}
		b.WriteString(")\n\n")
	}

	// Validate() method
	b.WriteString(fmt.Sprintf("// Validate checks the field values of %s against its validation rules.\n", msg.MessageName))
	b.WriteString(fmt.Sprintf("func (m *%s) Validate() error {\n", msg.MessageName))
	b.WriteString("\tvar errs []string\n")

	for fieldName, rules := range msg.Fields {
		goField := toPascalCase(fieldName)
		for _, rule := range rules {
			snippet := g.ruleToGo(goField, fieldName, rule)
			if snippet != "" {
				b.WriteString(snippet)
			}
		}
	}

	b.WriteString("\tif len(errs) > 0 {\n")
	b.WriteString(fmt.Sprintf("\t\treturn fmt.Errorf(\"validation failed for %s: %%s\", strings.Join(errs, \"; \"))\n", msg.MessageName))
	b.WriteString("\t}\n")
	b.WriteString("\treturn nil\n")
	b.WriteString("}\n")

	return b.String(), nil
}

func (g *GoCodeGenerator) ruleToGo(goField, protoField string, rule FieldRule) string {
	accessor := fmt.Sprintf("m.%s", goField)
	switch rule.Type {
	case RuleRequired:
		return fmt.Sprintf("\tif %s == \"\" {\n\t\terrs = append(errs, \"%s: is required\")\n\t}\n", accessor, protoField)
	case RuleGte:
		return fmt.Sprintf("\tif %s < %v {\n\t\terrs = append(errs, \"%s: must be >= %v\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleLte:
		return fmt.Sprintf("\tif %s > %v {\n\t\terrs = append(errs, \"%s: must be <= %v\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleGt:
		return fmt.Sprintf("\tif %s <= %v {\n\t\terrs = append(errs, \"%s: must be > %v\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleLt:
		return fmt.Sprintf("\tif %s >= %v {\n\t\terrs = append(errs, \"%s: must be < %v\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleConst:
		return fmt.Sprintf("\tif %s != %v {\n\t\terrs = append(errs, \"%s: must equal %v\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleMinLen:
		return fmt.Sprintf("\tif len([]rune(%s)) < %v {\n\t\terrs = append(errs, \"%s: length must be >= %v\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleMaxLen:
		return fmt.Sprintf("\tif len([]rune(%s)) > %v {\n\t\terrs = append(errs, \"%s: length must be <= %v\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RulePattern:
		return fmt.Sprintf("\tif matched, _ := regexp.MatchString(%q, %s); !matched {\n\t\terrs = append(errs, \"%s: must match pattern '%s'\")\n\t}\n", rule.Value, accessor, protoField, rule.Value)
	case RuleEmail:
		if v, ok := rule.Value.(bool); ok && v {
			return fmt.Sprintf("\tif %s != \"\" {\n\t\tif _, err := mail.ParseAddress(%s); err != nil {\n\t\t\terrs = append(errs, \"%s: must be a valid email\")\n\t\t}\n\t}\n", accessor, accessor, protoField)
		}
	case RuleURI:
		if v, ok := rule.Value.(bool); ok && v {
			return fmt.Sprintf("\tif %s != \"\" {\n\t\tif _, err := url.ParseRequestURI(%s); err != nil {\n\t\t\terrs = append(errs, \"%s: must be a valid URI\")\n\t\t}\n\t}\n", accessor, accessor, protoField)
		}
	case RuleUUID:
		if v, ok := rule.Value.(bool); ok && v {
			return fmt.Sprintf("\tif %s != \"\" && !regexp.MustCompile(`^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}$`).MatchString(%s) {\n\t\terrs = append(errs, \"%s: must be a valid UUID\")\n\t}\n", accessor, accessor, protoField)
		}
	case RuleNotEmpty:
		if v, ok := rule.Value.(bool); ok && v {
			return fmt.Sprintf("\tif strings.TrimSpace(%s) == \"\" {\n\t\terrs = append(errs, \"%s: must not be empty\")\n\t}\n", accessor, protoField)
		}
	case RulePrefix:
		return fmt.Sprintf("\tif !strings.HasPrefix(%s, %q) {\n\t\terrs = append(errs, \"%s: must have prefix '%s'\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleSuffix:
		return fmt.Sprintf("\tif !strings.HasSuffix(%s, %q) {\n\t\terrs = append(errs, \"%s: must have suffix '%s'\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleContains:
		return fmt.Sprintf("\tif !strings.Contains(%s, %q) {\n\t\terrs = append(errs, \"%s: must contain '%s'\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleMinItems:
		return fmt.Sprintf("\tif len(%s) < %v {\n\t\terrs = append(errs, \"%s: must have at least %v items\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleMaxItems:
		return fmt.Sprintf("\tif len(%s) > %v {\n\t\terrs = append(errs, \"%s: must have at most %v items\")\n\t}\n", accessor, rule.Value, protoField, rule.Value)
	case RuleIP:
		if v, ok := rule.Value.(bool); ok && v {
			return fmt.Sprintf("\tif %s != \"\" && net.ParseIP(%s) == nil {\n\t\terrs = append(errs, \"%s: must be a valid IP address\")\n\t}\n", accessor, accessor, protoField)
		}
	}
	return ""
}

func (g *GoCodeGenerator) resolveImports(msg MessageRules) []string {
	imps := map[string]bool{"fmt": true, "strings": true}
	for _, rules := range msg.Fields {
		for _, rule := range rules {
			switch rule.Type {
			case RulePattern, RuleUUID:
				imps["regexp"] = true
			case RuleEmail:
				imps["net/mail"] = true
			case RuleURI:
				imps["net/url"] = true
			case RuleIP, RuleIPv4, RuleIPv6:
				imps["net"] = true
			}
		}
	}
	result := make([]string, 0, len(imps))
	for k := range imps {
		result = append(result, k)
	}
	// Sort for deterministic output
	sortStrings(result)
	return result
}

