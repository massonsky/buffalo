// RustCodeGenerator generates Rust Validate trait implementations.
type RustCodeGenerator struct{}

func (g *RustCodeGenerator) Language() string { return "rust" }

func (g *RustCodeGenerator) Generate(messages []MessageRules) ([]GeneratedFile, error) {
	var files []GeneratedFile
	for _, msg := range messages {
		if msg.Disabled {
			continue
		}
		code := g.generateForMessage(msg)
		files = append(files, GeneratedFile{
			Path:    toSnakeCase(msg.MessageName) + "_validate.rs",
			Content: code,
		})
	}
	return files, nil
}

func (g *RustCodeGenerator) generateForMessage(msg MessageRules) string {
	var b strings.Builder
	b.WriteString("// Code generated by buffalo-validate. DO NOT EDIT.\n\n")

	b.WriteString(fmt.Sprintf("impl %s {\n", msg.MessageName))
	b.WriteString("    /// Validate checks all field validation rules.\n")
	b.WriteString("    pub fn validate(&self) -> Result<(), Vec<String>> {\n")
	b.WriteString("        let mut errors: Vec<String> = Vec::new();\n")

	for fieldName, rules := range msg.Fields {
		for _, rule := range rules {
			snippet := g.ruleToRust(fieldName, rule)
			if snippet != "" {
				b.WriteString(snippet)
			}
		}
	}

	b.WriteString("        if errors.is_empty() { Ok(()) } else { Err(errors) }\n")
	b.WriteString("    }\n}\n")
	return b.String()
}

func (g *RustCodeGenerator) ruleToRust(field string, rule FieldRule) string {
	accessor := fmt.Sprintf("self.%s", field)
	switch rule.Type {
	case RuleRequired:
		return fmt.Sprintf("        if %s.is_empty() {\n            errors.push(format!(\"%s: is required\"));\n        }\n", accessor, field)
	case RuleGte:
		return fmt.Sprintf("        if %s < %v_f64 {\n            errors.push(format!(\"%s: must be >= %v\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleLte:
		return fmt.Sprintf("        if %s > %v_f64 {\n            errors.push(format!(\"%s: must be <= %v\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleGt:
		return fmt.Sprintf("        if %s <= %v_f64 {\n            errors.push(format!(\"%s: must be > %v\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleLt:
		return fmt.Sprintf("        if %s >= %v_f64 {\n            errors.push(format!(\"%s: must be < %v\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleConst:
		return fmt.Sprintf("        if %s != %v_f64 {\n            errors.push(format!(\"%s: must equal %v\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleMinLen:
		return fmt.Sprintf("        if %s.len() < %v {\n            errors.push(format!(\"%s: length must be >= %v\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleMaxLen:
		return fmt.Sprintf("        if %s.len() > %v {\n            errors.push(format!(\"%s: length must be <= %v\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleNotEmpty:
		if v, ok := rule.Value.(bool); ok && v {
			return fmt.Sprintf("        if %s.trim().is_empty() {\n            errors.push(format!(\"%s: must not be empty\"));\n        }\n", accessor, field)
		}
	case RuleMinItems:
		return fmt.Sprintf("        if %s.len() < %v {\n            errors.push(format!(\"%s: must have at least %v items\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	case RuleMaxItems:
		return fmt.Sprintf("        if %s.len() > %v {\n            errors.push(format!(\"%s: must have at most %v items\"));\n        }\n", accessor, rule.Value, field, rule.Value)
	}
	return ""
}

// ══════════════════════════════════════════════════════════════════
//  Small utility
// ══════════════════════════════════════════════════════════════════

func sortStrings(s []string) {
	for i := 1; i < len(s); i++ {
		for j := i; j > 0 && s[j] < s[j-1]; j-- {
			s[j], s[j-1] = s[j-1], s[j]
		}
	}
}
