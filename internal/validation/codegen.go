// CppCodeGenerator generates C++ validation functions as a header + source pair.
type CppCodeGenerator struct{}

func (g *CppCodeGenerator) Language() string { return "cpp" }

func (g *CppCodeGenerator) Generate(messages []MessageRules) ([]GeneratedFile, error) {
	var files []GeneratedFile
	for _, msg := range messages {
		if msg.Disabled {
			continue
		}
		header, source := g.generateForMessage(msg)
		base := toSnakeCase(msg.MessageName)
		files = append(files,
			GeneratedFile{Path: base + ".validate.h", Content: header},
			GeneratedFile{Path: base + ".validate.cc", Content: source},
		)
	}
	return files, nil
}

func (g *CppCodeGenerator) generateForMessage(msg MessageRules) (string, string) {
	guard := strings.ToUpper(toSnakeCase(msg.MessageName)) + "_VALIDATE_H_"
	base := toSnakeCase(msg.MessageName)

	// Header
	var h strings.Builder
	h.WriteString("// Code generated by buffalo-validate. DO NOT EDIT.\n")
	h.WriteString(fmt.Sprintf("#ifndef %s\n#define %s\n\n", guard, guard))
	h.WriteString("#include <string>\n#include <vector>\n\n")
	h.WriteString(fmt.Sprintf("std::vector<std::string> Validate%s(const %s& msg);\n\n", msg.MessageName, msg.MessageName))
	h.WriteString(fmt.Sprintf("#endif // %s\n", guard))

	// Source
	var s strings.Builder
	s.WriteString("// Code generated by buffalo-validate. DO NOT EDIT.\n")
	s.WriteString(fmt.Sprintf("#include \"%s.validate.h\"\n\n", base))
	s.WriteString(fmt.Sprintf("std::vector<std::string> Validate%s(const %s& msg) {\n", msg.MessageName, msg.MessageName))
	s.WriteString("    std::vector<std::string> errors;\n")

	for fieldName, rules := range msg.Fields {
		accessor := fmt.Sprintf("msg.%s()", fieldName)
		for _, rule := range rules {
			snippet := g.ruleToCpp(accessor, fieldName, rule)
			if snippet != "" {
				s.WriteString(snippet)
			}
		}
	}

	s.WriteString("    return errors;\n}\n")
	return h.String(), s.String()
}

func (g *CppCodeGenerator) ruleToCpp(accessor, field string, rule FieldRule) string {
	switch rule.Type {
	case RuleRequired:
		return fmt.Sprintf("    if (%s.empty()) errors.push_back(\"%s: is required\");\n", accessor, field)
	case RuleGte:
		return fmt.Sprintf("    if (%s < %v) errors.push_back(\"%s: must be >= %v\");\n", accessor, rule.Value, field, rule.Value)
	case RuleLte:
		return fmt.Sprintf("    if (%s > %v) errors.push_back(\"%s: must be <= %v\");\n", accessor, rule.Value, field, rule.Value)
	case RuleGt:
		return fmt.Sprintf("    if (%s <= %v) errors.push_back(\"%s: must be > %v\");\n", accessor, rule.Value, field, rule.Value)
	case RuleLt:
		return fmt.Sprintf("    if (%s >= %v) errors.push_back(\"%s: must be < %v\");\n", accessor, rule.Value, field, rule.Value)
	case RuleConst:
		return fmt.Sprintf("    if (%s != %v) errors.push_back(\"%s: must equal %v\");\n", accessor, rule.Value, field, rule.Value)
	case RuleMinLen:
		return fmt.Sprintf("    if (%s.size() < %v) errors.push_back(\"%s: length must be >= %v\");\n", accessor, rule.Value, field, rule.Value)
	case RuleMaxLen:
		return fmt.Sprintf("    if (%s.size() > %v) errors.push_back(\"%s: length must be <= %v\");\n", accessor, rule.Value, field, rule.Value)
	case RuleNotEmpty:
		if v, ok := rule.Value.(bool); ok && v {
			return fmt.Sprintf("    if (%s.empty()) errors.push_back(\"%s: must not be empty\");\n", accessor, field)
		}
	}
	return ""
}

