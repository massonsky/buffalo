// ──────────────────────────────────────────────────────────────────
// CodeGenerator interface
// ──────────────────────────────────────────────────────────────────

// CodeGenerator generates validation source code for a target language.
type CodeGenerator interface {
	// Language returns the target language identifier.
	Language() string

	// Generate produces validation code for the given messages.
	Generate(messages []MessageRules) ([]GeneratedFile, error)
}

// GeneratedFile represents a single generated source file.
type GeneratedFile struct {
	Path    string // Relative output path
	Content string // Generated source code
}

// NewCodeGenerator returns a CodeGenerator for the given language.
func NewCodeGenerator(language string) (CodeGenerator, error) {
	switch language {
	case "go":
		return &GoCodeGenerator{}, nil
	case "python":
		return &PythonCodeGenerator{}, nil
	case "cpp":
		return &CppCodeGenerator{}, nil
	case "rust":
		return &RustCodeGenerator{}, nil
	default:
		return nil, fmt.Errorf("unsupported validation codegen language: %s", language)
	}
}

// ──────────────────────────────────────────────────────────────────
// Helpers shared by all generators
// ──────────────────────────────────────────────────────────────────

// toSnakeCase converts CamelCase to snake_case.
func toSnakeCase(s string) string {
	var b strings.Builder
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			b.WriteByte('_')
		}
		if r >= 'A' && r <= 'Z' {
			b.WriteRune(r + 32) // toLower
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}

// toPascalCase converts a snake_case identifier to PascalCase.
func toPascalCase(s string) string {
	parts := strings.Split(s, "_")
	for i := range parts {
		if len(parts[i]) > 0 {
			parts[i] = strings.ToUpper(parts[i][:1]) + parts[i][1:]
		}
	}
	return strings.Join(parts, "")
}

// goPackageName extracts the last component of a dotted package name.
func goPackageName(pkg string) string {
	parts := strings.Split(pkg, ".")
	return parts[len(parts)-1]
}

